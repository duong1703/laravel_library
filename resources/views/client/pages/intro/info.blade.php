                    7/1997, Bộ Công nghiệp quyết định đổi tên thành trường Trung học Điện 1. Tháng 04/2000, Bộ Công
                    nghiệp quyết định hợp nhất Trường Trung học Điện 1 và Trường Bồi dưỡng Tại chức thành Trường Trung
                    học Điện 1 thuộc Tổng Công ty Điện lực Việt Nam (nay là Tập đoàn Điện lực Việt Nam). Ngày
                    26/10/2001, Bộ trưởng Bộ Giáo dục và Đào tạo ký quyết định thành lập Trường Cao đẳng Điện lực trên
