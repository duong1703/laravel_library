                <p>Trường Đại học Điện lực là một trường đại học công lập đa cấp, đa ngành có nhiệm vụ chủ yếu là đào
                    tạo nguồn nhân lực có chất lượng cao cung cấp cho Ngành và phục vụ nhu cầu kinh tế xã hội đồng thời
