                    ký quyết định thành lập Trường Đại học Điện lực trên cơ sở Trường Cao đẳng Điện lực. Thực hiện Nghị
                    quyết 77 NQ-CP của Chính phủ về đổi mới cơ chế đào tạo, trên cơ sở nhiều năm liên tục tự chủ về kinh
                    phí trong các hoạt động của Trường; ngày 01/9/2015 Thủ tướng Chính phủ ký quyết định số 1508/QĐ-TTg
