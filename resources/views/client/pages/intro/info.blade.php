                    trường Đại học Điện lực luôn thực hiện các nhiệm vụ KH&CN nhằm đáp ứng các nhu cầu của khách hàng và
                    thực tiễn đặt ra. Các lĩnh vực nghiên cứu thuộc thế mạnh của trường bao gồm: Các hệ thống kỹ thuật
                    trong các nhà máy điện (thủy điện, nhiệt điện, điện hạt nhân, điện gió, điện mặt trời, điện sinh
                    khối, điện rác thải), trạm điện và đường dây truyền tải điện; các bộ phận và hệ thống nhiệt-lạnh
                    (như lò hơi, lò quay, máy lạnh, tuabin nhiệt, bơm nhiệt , mạng nhiệt); các hệ thống đo lường, điều
                    khiển, bảo vệ, giám sát và truyền tin trên diện rộng; các vấn đề liên quan đến cơ khí, chế tạo và
                    xây dựng điện....

                    Nhiều năm qua trường đã nghiên cứu hàng trăm đề tài các cấp như: cấp Trường, cấp EVN, cấp Bộ Công
